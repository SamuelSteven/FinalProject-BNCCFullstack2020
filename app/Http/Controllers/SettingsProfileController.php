     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $request->validate([
            'name' => ['required'],
            'username' => ['required'],
            'email' => ['required']
        ]);
        
        User::where('id',$id)
            ->update([
                'name' => $request->name,
                'username' => $request->username,
                'email' => $request->email
            ]);
        return redirect('/home')->with('status','Profile Edited Successfully!'); 
    }

