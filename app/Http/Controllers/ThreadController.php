    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $questions = question::find($id);
        $answer = answer::where('questionId','=',$id)->get();
        $answer_count = $answer->count();
        if ($answer_count > 0){
            foreach($answer as $keys => $a){
                $reply[] = reply::where('answerId','=',$a->id)->get();
                $reply_count[] = $reply[$keys]->count();
            }
        } else{
            $reply = NULL;
            $reply_count = 0;
        }
        return view('/thread_no_login', compact('questions', 'answer', 'answer_count', 'reply', 'reply_count'));
    }

